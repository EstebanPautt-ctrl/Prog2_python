    public void setTamaño(int n) {
        this.n = n;
        crearMatriz();
    }
    
    public void crearMatriz(){
        pnlMatriz.removeAll();
        pnlMatriz.setLayout(new GridLayout(n,n,5,5));
        
        txtDigit = new JTextField[n][n];
        
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) {
                txtDigit[i][j] = new JTextField(3);
                pnlMatriz.add(txtDigit[i][j]);
            }
        }
        
        pnlMatriz.revalidate();
        pnlMatriz.repaint();
        
        int size = Math.min(600, n*50);
        setSize(size, size + 70);
        setLocationRelativeTo(null);
        
        
    }
    
    private void imprimirDiagonales(){
        int[][] matriz = new int[n][n];
        
        try{
            for (int i = 0; i < n; i++){
                for (int j = 0; j < n; j++) {
                    matriz[i][j] = Integer.parseInt(txtDigit[i][j].getText());
                }
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ingrese solo números enteros en la matriz.");
            return;
        }
        StringBuilder sb = new StringBuilder("Diagonal Principal: ");
        for (int i = 0; i < n; i++) {
            sb.append(matriz[i][i]).append(" ");
        }

        sb.append("\nDiagonal Secundaria: ");
        for (int i = 0; i < n; i++) {
            sb.append(matriz[i][n - 1 - i]).append(" ");
        }

        JOptionPane.showMessageDialog(this, sb.toString());
    }

